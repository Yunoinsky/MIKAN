#include "print.h"

static volatile uint8_t *buf = 0;
static uint32_t w, h, pitch;

static uint32_t x, y;

#define CHAR_W  6
#define CHAR_H  8
#define TEX_W   96
#define TEX_H   48
static uint8_t tex_data[TEX_H * TEX_W];

void print_init(uint8_t *_buf, uint32_t _w, uint32_t _h, uint32_t _pitch)
{
    buf = _buf;
    w = _w;
    h = _h;
    pitch = _pitch;
    x = y = 0;
}

void print_putchar(char ch)
{
    if (ch == '\n') {
        x = 0;
        if ((y += CHAR_H) > h - CHAR_H) y = 0;
        return;
    } else if (ch == '\r') {
        x = 0;
        return;
    }

    uint16_t x0 = (ch - 32) * CHAR_W;
    uint16_t tx = x0 % TEX_W;
    uint16_t ty = x0 / TEX_W * CHAR_H;

    for (uint8_t dx = 0; dx < CHAR_W; dx++)
    for (uint8_t dy = 0; dy < CHAR_H; dy++) {
        buf[(y + dy) * pitch + (x + dx) * 3 + 0] =
        buf[(y + dy) * pitch + (x + dx) * 3 + 1] =
        buf[(y + dy) * pitch + (x + dx) * 3 + 2] =
            tex_data[(ty + dy) * TEX_W + (tx + dx)] * 255;
    }

    if ((x += CHAR_W) > w - CHAR_W) {
        x = 0;
        if ((y += CHAR_H) > h - CHAR_H) y = 0;
    }
}

void LogPrint(const char *str, uint32_t len)
{
    while (*str != '\0') print_putchar(*str++);
}

